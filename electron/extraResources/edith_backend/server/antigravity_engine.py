def generate_research_memo(
    intent: str,
    code: str,
    output: str,
    model_chain: list[str] = None,
) -> dict:
    """Transform raw statistical output into a research memo.

    The memo explains findings in academic prose, cites relevant
    literature, and flags methodological concerns.
    """
    model_chain = model_chain or [os.environ.get("EDITH_MODEL", "gemini-2.5-flash")]

    sig = thought_signer.sign_thought(
        f"Research Memo: {intent}",
        grounding=["output_analysis"],
    )

    try:
        from server.backend_logic import generate_text_via_chain
        prompt = (
            f"RESEARCH INTENT: {intent}\n\n"
            f"CODE RUN:\n{code[:1000]}\n\n"
            f"RAW OUTPUT:\n{output[:2000]}\n\n"
            f"Generate a RESEARCH MEMO with:\n"
            f"1. **Key Finding**: One sentence summary\n"
            f"2. **Interpretation**: What this means substantively\n"
            f"3. **Statistical Details**: Coefficients, SEs, significance\n"
            f"4. **Diagnostics**: Model fit, assumption checks\n"
            f"5. **Caveats**: Limitations of this analysis\n"
            f"6. **Next Steps**: What to run next\n"
            f"7. **Relevant Literature**: Papers that support/challenge this finding\n\n"
            f"Write in academic prose suitable for a dissertation methods chapter."
        )
        memo, model = generate_text_via_chain(
            prompt, model_chain,
            system_instruction=(
                "You are a research methodology expert writing a concise memo "
                "for a PhD student. Be precise about statistical interpretation. "
                "Never overstate significance."
            ),
            temperature=0.2,
        )
        return {
            "intent": intent,
            "memo": memo,
            "thought_signature": sig["signature"],
            "model": model,
        }
    except Exception as e:
        return {
            "intent": intent,
            "memo": f"## Research Memo: {intent}\n\n[LLM unavailable — raw output attached]\n\n```\n{output[:1000]}\n```",
            "thought_signature": sig["signature"],
            "error": str(e),
        }


