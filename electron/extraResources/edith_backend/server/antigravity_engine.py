class ThoughtSignature:
    """Prevents reasoning drift by signing every planning step.

    Each thought is hashed with the previous signature,
    forming an immutable chain of reasoning.
    """

    def __init__(self):
        self._chain: list[dict] = []
        self._current_hash = "GENESIS"

    def sign_thought(self, thought: str, grounding: list[str] = None) -> dict:
        """Sign a reasoning step and add it to the chain."""
        payload = {
            "step": len(self._chain) + 1,
            "thought": thought,
            "grounding": grounding or [],  # Sources from the vault
            "previous_hash": self._current_hash,
            "timestamp": datetime.now().isoformat(),
        }

        # Chain the hash
        sig_input = f"{self._current_hash}|{thought}|{json.dumps(grounding or [])}"
        signature = hashlib.sha256(sig_input.encode()).hexdigest()[:16]
        payload["signature"] = signature
        self._current_hash = signature

        self._chain.append(payload)
        return payload

    def verify_chain(self) -> dict:
        """Verify the entire reasoning chain for integrity."""
        if not self._chain:
            return {"valid": True, "steps": 0}

        prev = "GENESIS"
        for i, step in enumerate(self._chain):
            sig_input = f"{prev}|{step['thought']}|{json.dumps(step['grounding'])}"
            expected = hashlib.sha256(sig_input.encode()).hexdigest()[:16]
            if expected != step["signature"]:
                return {
                    "valid": False,
                    "broken_at": i + 1,
                    "expected": expected,
                    "got": step["signature"],
                }
            prev = step["signature"]

        return {"valid": True, "steps": len(self._chain)}

    def get_chain(self) -> list[dict]:
        return list(self._chain)

    def reset(self):
        self._chain = []
        self._current_hash = "GENESIS"


