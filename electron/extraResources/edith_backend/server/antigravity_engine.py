# GLASS BOX §1: LOGIC TRACE — "Rationalization Artifact"
# ═══════════════════════════════════════════════════════════════════

def generate_logic_trace(
    code: str,
    intent: str,
    data_context: str = "",
    model_chain: list[str] = None,
) -> dict:
    """Generate a "Rationalization Artifact" — explain WHY this code was written.

    Reads the ThoughtSignature chain, the matched template, data context,
    and field notes to produce a natural-language methodological audit.

    The user clicks "Logic Trace" in the Cockpit and sees:
        "I used a fixed-effects model here because your 2024 field notes
         from Lubbock suggested county-level variance was non-random.
         A standard OLS would have ignored the 'Charity Gatekeeping' bias
         I detected in the metadata."

    Returns:
        dict with: rationale, method_justification, data_signals,
                   alternative_methods, thought_chain
    """
    model_chain = model_chain or [os.environ.get("EDITH_MODEL", "gemini-2.5-flash")]

    # 1. Read the reasoning chain
    chain = thought_signer.get_chain()
    chain_summary = []
    for step in chain[-5:]:  # Last 5 steps
        chain_summary.append({
            "thought": step.get("thought", ""),
            "grounding": step.get("grounding", []),
        })

    # 2. Detect which template/method was used
    code_lower = code.lower()
    method_detected = "custom"
    method_rationale = ""

    for name, template in INTENT_TEMPLATES.items():
        if any(kw in code_lower for kw in template["keywords"]):
            method_detected = name
            break

    METHOD_RATIONALES = {
        "multi_level_model": (
            "Multi-level modeling accounts for nested data structure "
            "(e.g., individuals within counties). Standard OLS would "
            "violate the independence assumption and underestimate "
            "standard errors."
        ),
        "difference_in_differences": (
            "DiD exploits a natural experiment — a policy change creates "
            "pre/post variation. This requires the parallel trends "
            "assumption, which should be tested."
        ),
        "regression_discontinuity": (
            "RDD uses a threshold/cutoff as a quasi-random assignment "
            "mechanism. Units just above and below the cutoff are "
            "comparable, giving strong internal validity."
        ),
        "instrumental_variables": (
            "IV addresses endogeneity — when the treatment variable is "
            "correlated with unobservable confounders. The instrument "
            "must satisfy relevance and exclusion."
        ),
    }

    method_rationale = METHOD_RATIONALES.get(method_detected, (
        "A custom model was constructed based on the specific "
        "structure of the data and research question."
    ))

    # 3. Detect data-driven signals in the code
    data_signals = []
    if "cluster" in code_lower:
        data_signals.append("Clustered SEs used — data has group structure")
    if "absorb" in code_lower or "fe" in code_lower:
        data_signals.append("Fixed effects absorb unit-level confounders")
    if "robust" in code_lower:
        data_signals.append("Robust SEs guard against heteroskedasticity")
    if "vif" in code_lower:
        data_signals.append("VIF check — testing for collinearity")
    if "icc" in code_lower:
        data_signals.append("ICC computed — variance partition across levels")

    # 4. Alternative methods considered
    alternatives = []
    for name, template in INTENT_TEMPLATES.items():
        if name != method_detected:
            alternatives.append({
                "method": name.replace("_", " ").title(),
                "why_not": f"Not selected for this intent: '{intent[:50]}'",
            })

    # 5. Try LLM-enhanced rationalization if available
    llm_rationale = ""
    try:
        from server.backend_logic import generate_text_via_chain
        prompt = (
            f"INTENT: {intent}\n"
            f"CODE:\n{code[:800]}\n"
            f"METHOD: {method_detected}\n"
            f"DATA CONTEXT: {data_context[:300]}\n\n"
            f"In 3-4 sentences, explain WHY this specific method was chosen "
            f"for this research question. Reference the data structure, "
            f"assumptions required, and any signals in the research context "
            f"that made this the best approach. Write in first person as "
            f"Winnie (the AI research assistant)."
        )
        llm_rationale, _ = generate_text_via_chain(
            prompt, model_chain,
            system_instruction=(
                "You are explaining your methodological reasoning to a "
                "PhD student. Be precise about WHY, not just WHAT."
            ),
            temperature=0.2,
        )
    except Exception:
        llm_rationale = method_rationale

    sig = thought_signer.sign_thought(
        f"Logic Trace: {method_detected} for '{intent[:50]}'",
        grounding=[method_detected] + data_signals[:2],
    )

    return {
        "intent": intent,
        "method_detected": method_detected,
        "method_rationale": method_rationale,
        "llm_rationale": llm_rationale,
        "data_signals": data_signals,
        "alternatives_considered": alternatives,
        "thought_chain": chain_summary,
        "thought_signature": sig["signature"],
        "glass_box": True,
    }
