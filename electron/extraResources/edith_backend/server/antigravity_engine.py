class ResearchSkill:
    """Learns and enforces your personal coding/writing style.

    Tracks: variable naming, table formatting, citation style,
    preferred methods, and writing voice.
    """

    DEFAULT_SKILL = {
        "variable_naming": {
            "convention": "snake_case",
            "prefix_dv": "",
            "prefix_iv": "",
            "examples": {
                "voter_turnout": "Voter turnout (0-1)",
                "charity_density": "Charities per 10K pop",
                "snap_enrollment": "SNAP enrollment rate",
                "income_median": "Median household income",
            },
        },
        "table_formatting": {
            "style": "APA",
            "stars": "* p<0.10, ** p<0.05, *** p<0.01",
            "se_format": "parentheses",
            "includes": ["N", "R²", "Cluster FE", "Year FE"],
        },
        "citation_style": "APSA",
        "preferred_methods": [
            "multi-level models (xtmixed/lmer)",
            "difference-in-differences (Callaway-Sant'Anna)",
            "regression discontinuity (rdrobust)",
            "instrumental variables (2SLS)",
        ],
        "writing_voice": {
            "tone": "precise_academic",
            "person": "third_person_passive",
            "hedging": "moderate",
            "examples": [
                "Results suggest that charity density is negatively associated with SNAP enrollment.",
                "The coefficient on the interaction term is statistically significant at the 5% level.",
            ],
        },
        "stata_preferences": {
            "se_type": "cluster",
            "fe_package": "reghdfe",
            "ml_package": "xtmixed",
            "output_format": "esttab",
        },
        "r_preferences": {
            "se_type": "cluster",
            "fe_package": "fixest",
            "ml_package": "lme4",
            "output_format": "stargazer",
        },
    }

    def __init__(self, data_root: str = ""):
        self._data_root = data_root or os.environ.get("EDITH_DATA_ROOT", ".")
        self._skill_path = os.path.join(self._data_root, ".edith_research_skill.json")
        self._skill = self._load_skill()

    def _load_skill(self) -> dict:
        if os.path.exists(self._skill_path):
            try:
                with open(self._skill_path) as f:
                    return json.load(f)
            except Exception:
                pass
        return dict(self.DEFAULT_SKILL)

    def save_skill(self) -> dict:
        os.makedirs(os.path.dirname(self._skill_path) or ".", exist_ok=True)
        with open(self._skill_path, "w") as f:
            json.dump(self._skill, f, indent=2)
        return {"status": "saved", "path": self._skill_path}

    def get_skill(self) -> dict:
        return dict(self._skill)

    def update_skill(self, updates: dict) -> dict:
        """Update specific skill preferences."""
        for key, value in updates.items():
            if key in self._skill:
                if isinstance(self._skill[key], dict) and isinstance(value, dict):
                    self._skill[key].update(value)
                else:
                    self._skill[key] = value
        self.save_skill()
        return {"status": "updated", "skill": self._skill}

    def learn_from_code(self, code: str, language: str = "stata") -> dict:
        """Analyze existing code to learn style preferences."""
        learned = {}
        code_lower = code.lower()

        # Detect naming convention
        vars_found = re.findall(r'\b([a-z][a-z0-9_]+)\b', code)
        if vars_found:
            snake = sum(1 for v in vars_found if "_" in v)
            camel = sum(1 for v in vars_found if re.match(r'^[a-z]+[A-Z]', v))
            learned["naming"] = "snake_case" if snake > camel else "camelCase"

        # Detect SE preferences
        if "robust" in code_lower or "cluster" in code_lower:
            learned["se_type"] = "cluster" if "cluster" in code_lower else "robust"

        # Detect FE packages
        if "reghdfe" in code_lower:
            learned["fe_package"] = "reghdfe"
        elif "areg" in code_lower:
            learned["fe_package"] = "areg"
        if "fixest" in code_lower or "feols" in code_lower:
            learned["r_fe_package"] = "fixest"

        # Detect output format
        if "esttab" in code_lower:
            learned["output_format"] = "esttab"
        elif "outreg" in code_lower:
            learned["output_format"] = "outreg2"
        elif "stargazer" in code_lower:
            learned["r_output_format"] = "stargazer"

        return {"learned_preferences": learned}

    def apply_to_code(self, code: str, language: str = "stata") -> str:
        """Apply your style preferences to generated code."""
        prefs = self._skill.get(f"{language}_preferences", {})

        # Apply SE preference
        if prefs.get("se_type") == "cluster":
            if language == "stata":
                code = re.sub(r',\s*robust\b', ', cluster({cluster_var})', code)
            elif language == "r":
                code = re.sub(r'vcov\s*=\s*"HC1"', 'cluster = ~{cluster_var}', code)

        return code


