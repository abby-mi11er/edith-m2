def _sandbox_run(code: str, language: str, timeout: int = 10) -> dict:
    """Run code in a restricted subprocess sandbox and return result.

    Returns {"success": bool, "stdout": str, "stderr": str, "exit_code": int}
    """
    import tempfile
    cmd_map = {
        "python": ["python3", "-c"],
        "r": ["Rscript", "-e"],
        "stata": None,  # Stata can't be sandboxed easily
    }
    cmd = cmd_map.get(language.lower())
    if not cmd:
        # Can't sandbox this language — skip verification
        return {"success": None, "stdout": "", "stderr": "sandbox_unavailable", "exit_code": -1}

    try:
        result = subprocess.run(
            [*cmd, code],
            capture_output=True, text=True,
            timeout=timeout,
            env={**os.environ, "OPENBLAS_NUM_THREADS": "1"},
        )
        return {
            "success": result.returncode == 0,
            "stdout": result.stdout[:2000],
            "stderr": result.stderr[:2000],
            "exit_code": result.returncode,
        }
    except subprocess.TimeoutExpired:
        return {"success": False, "stdout": "", "stderr": "Execution timed out", "exit_code": -1}
    except FileNotFoundError:
        return {"success": None, "stdout": "", "stderr": f"{cmd[0]} not found", "exit_code": -1}
    except Exception as e:
        return {"success": False, "stdout": "", "stderr": str(e), "exit_code": -1}


def self_heal_script(
    error_output: str,
    original_code: str,
    language: str = "stata",
    model_chain: list[str] = None,
    verify: bool = False,
    max_attempts: int = 3,
) -> dict:
    """Diagnose a script failure and generate a fixed version.

    Matches error patterns, applies known fixes, and regenerates.

    If verify=True, runs the fix in a subprocess sandbox and retries
    up to max_attempts times if the fix itself fails (agentic loop).
    """
    model_chain = model_chain or [os.environ.get("EDITH_MODEL", "gemini-2.5-flash")]

    # Pattern matching
    matched_patterns = []
    for pattern, fix_info in SELF_HEAL_PATTERNS.items():
        if re.search(pattern, error_output, re.IGNORECASE):
            matched_patterns.append(fix_info)

    sig = thought_signer.sign_thought(
        f"Self-heal: {len(matched_patterns)} patterns matched",
        grounding=[p["diagnosis"] for p in matched_patterns],
    )

    def _generate_fix(ctx: dict) -> dict:
        """Generate a fix — LLM for unknown errors, template for known."""
        current_error = ctx.get("_last_error", error_output)
        current_code = ctx.get("_last_code", original_code)
        attempt = ctx.get("_attempt", 1)

        if matched_patterns and attempt == 1:
            # First attempt: use template
            fix = matched_patterns[0]
            fix_key = f"{language}_fix"
            return {
                "diagnosis": fix["diagnosis"],
                "suggested_fixes": fix["fixes"],
                "fixed_code": fix.get(fix_key, ""),
                "patterns_matched": len(matched_patterns),
                "source": "template",
            }

        # LLM fix (for unknown errors, or retries after template failed)
        try:
            from server.backend_logic import generate_text_via_chain
            history_hint = ""
            if attempt > 1:
                history_hint = (
                    f"\n\nPREVIOUS FIX ATTEMPT FAILED with error:\n"
                    f"{current_error[:500]}\n"
                    f"Do NOT repeat the same fix. Try a different approach.\n"
                )
            prompt = (
                f"ERROR OUTPUT:\n{current_error[:1000]}\n\n"
                f"ORIGINAL CODE:\n{current_code[:1500]}\n\n"
                f"{history_hint}"
                f"Diagnose the error and generate a FIXED version of the code.\n"
                f"Language: {language}\n"
                f"Output ONLY the complete fixed script, no explanation."
            )
            fixed, model = generate_text_via_chain(
                prompt, model_chain,
                system_instruction="You are a debugging expert for statistical software.",
                temperature=0.1 + (attempt * 0.1),  # increase creativity on retries
            )
            return {
                "diagnosis": "LLM-diagnosed error" if attempt == 1 else f"LLM retry #{attempt}",
                "fixed_code": fixed,
                "patterns_matched": len(matched_patterns),
                "source": "llm",
                "model": model,
            }
        except Exception as e:
            return {"error": str(e), "fixed_code": ""}

    def _validate_fix(result: dict) -> dict:
        """Validate fix by running in sandbox."""
        code = result.get("fixed_code", "")
        if not code or result.get("error"):
            return {"valid": False, "error": result.get("error", "No code generated")}
        if not verify:
            return {"valid": True, "error": ""}  # skip verification
        sandbox = _sandbox_run(code, language)
        if sandbox["success"] is None:
            # Can't sandbox — treat as valid (user will verify)
            return {"valid": True, "error": "", "sandbox": "unavailable"}
        if sandbox["success"]:
            return {"valid": True, "error": "", "sandbox": "passed"}
        return {"valid": False, "error": sandbox["stderr"][:500]}

    # Run the agentic loop
    try:
        from server.agentic import agentic_loop
        loop_result = agentic_loop(
            action_fn=_generate_fix,
            validator_fn=_validate_fix,
            max_attempts=max_attempts if verify else 1,
            context={"_last_error": error_output, "_last_code": original_code},
        )
        inner = loop_result["result"]
        return {
            "diagnosis": inner.get("diagnosis", "Unknown"),
            "suggested_fixes": inner.get("suggested_fixes", []),
            "fixed_code": inner.get("fixed_code", ""),
            "fix_code": inner.get("fixed_code", ""),  # compat
            "patterns_matched": inner.get("patterns_matched", 0),
            "thought_signature": sig["signature"],
            "model": inner.get("model", inner.get("source", "template")),
            "requires_approval": not loop_result["verified"],
            "verified": loop_result["verified"],
            "attempts": loop_result["attempts"],
            "attempt_history": loop_result["history"],
        }
    except Exception as e:
        # Ultimate fallback — return simple template match
        if matched_patterns:
            fix = matched_patterns[0]
            return {
                "diagnosis": fix["diagnosis"],
                "suggested_fixes": fix["fixes"],
                "fix_code": fix.get(f"{language}_fix", ""),
                "patterns_matched": len(matched_patterns),
                "thought_signature": sig["signature"],
                "requires_approval": True,
                "verified": False,
            }
        return {"error": str(e)}


