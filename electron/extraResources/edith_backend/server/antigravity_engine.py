# §6: MISSION CONTROL — Asynchronous Agent Manager
# "Dispatch agents to the Bolt. One re-indexes while another simulates."
# ═══════════════════════════════════════════════════════════════════

class MissionControl:
    """The Manager surface — dispatch and track autonomous agents."""

    def __init__(self):
        self._agents: dict[str, dict] = {}
        self._lock = threading.Lock()

    def dispatch_agent(
        self,
        task_type: str,
        description: str,
        params: dict = None,
    ) -> dict:
        """Dispatch an autonomous agent to work in the background."""
        agent_id = f"agent_{int(time.time())}_{len(self._agents)}"

        agent = {
            "id": agent_id,
            "type": task_type,
            "description": description,
            "params": params or {},
            "status": "dispatched",
            "dispatched_at": datetime.now().isoformat(),
            "progress": 0,
            "result": None,
            "thought_chain": [],
        }

        with self._lock:
            self._agents[agent_id] = agent

        # Start background execution
        thread = threading.Thread(
            target=self._execute_agent, args=(agent_id,), daemon=True
        )
        thread.start()

        return {"agent_id": agent_id, "status": "dispatched", "type": task_type}

    def get_agent_status(self, agent_id: str) -> dict:
        with self._lock:
            return self._agents.get(agent_id, {"error": "Agent not found"})

    def list_agents(self) -> list[dict]:
        with self._lock:
            return [
                {k: v for k, v in a.items() if k != "thought_chain"}
                for a in self._agents.values()
            ]

    def get_all_status(self) -> dict:
        with self._lock:
            running = sum(1 for a in self._agents.values() if a["status"] == "running")
            done = sum(1 for a in self._agents.values() if a["status"] == "completed")
            return {
                "total": len(self._agents),
                "running": running,
                "completed": done,
                "failed": len(self._agents) - running - done,
            }

    def _execute_agent(self, agent_id: str):
        """Execute an agent's task."""
        agent = self._agents.get(agent_id)
        if not agent:
            return

        agent["status"] = "running"
        agent["started_at"] = datetime.now().isoformat()

        try:
            task_type = agent["type"]

            if task_type == "reindex":
                result = self._task_reindex(agent)
            elif task_type == "monte_carlo":
                result = self._task_monte_carlo(agent)
            elif task_type == "literature_scan":
                result = self._task_literature_scan(agent)
            elif task_type == "stress_test":
                result = self._task_stress_test(agent)
            elif task_type == "synthesis":
                result = self._task_synthesis(agent)
            else:
                result = {"error": f"Unknown task type: {task_type}"}

            agent["result"] = result
            agent["status"] = "completed"
        except Exception as e:
            agent["result"] = {"error": str(e)}
            agent["status"] = "failed"

        agent["completed_at"] = datetime.now().isoformat()

    def _task_reindex(self, agent: dict) -> dict:
        """Re-index documents on the Bolt."""
        agent["progress"] = 10
        time.sleep(0.1)  # Simulated work
        agent["progress"] = 50
        try:
            from server.completions import incremental_reindex
            result = incremental_reindex()
            agent["progress"] = 100
            return result
        except Exception:
            agent["progress"] = 100
            return {"status": "reindex_queued"}

    def _task_monte_carlo(self, agent: dict) -> dict:
        """Run Monte Carlo simulation."""
        agent["progress"] = 10
        try:
            from server.simulation_deck import AgentBasedModel
            n = agent["params"].get("n_agents", 5000)
            months = agent["params"].get("months", 12)
            abm = AgentBasedModel(n, agent["params"].get("county", "lubbock"))
            abm.populate()
            agent["progress"] = 30

            shock_type = agent["params"].get("shock_type", "snap_cut")
            shock_mag = agent["params"].get("shock_magnitude", 0.15)
            abm.introduce_shock(shock_type, shock_mag)
            agent["progress"] = 40

            result = abm.simulate_months(months)
            agent["progress"] = 100
            return result
        except Exception as e:
            return {"error": str(e)}

    def _task_literature_scan(self, agent: dict) -> dict:
        """Scan literature feeds."""
        agent["progress"] = 50
        try:
            from server.oracle_engine import heartbeat_monitor
            result = heartbeat_monitor.check_now()
            agent["progress"] = 100
            return result
        except Exception as e:
            return {"error": str(e)}

    def _task_stress_test(self, agent: dict) -> dict:
        """Run causal stress test."""
        agent["progress"] = 20
        try:
            from server.causal_engine import stress_test_causal_claim
            cause = agent["params"].get("cause", "charity density")
            effect = agent["params"].get("effect", "SNAP enrollment")
            result = stress_test_causal_claim(cause, effect)
            agent["progress"] = 100
            return result
        except Exception as e:
            return {"error": str(e)}

    def _task_synthesis(self, agent: dict) -> dict:
        """Find cross-domain synthesis."""
        agent["progress"] = 20
        try:
            from server.oracle_engine import find_synthesis_bridges
            result = find_synthesis_bridges(agent["params"].get("topic", ""))
            agent["progress"] = 100
            return result
        except Exception as e:
            return {"error": str(e)}


mission_control = MissionControl()


