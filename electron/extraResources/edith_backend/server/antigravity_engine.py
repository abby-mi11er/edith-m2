def tab_to_intent(
    intent: str,
    language: str = "stata",
    data_path: str = "",
    model_chain: list[str] = None,
) -> dict:
    """Convert a research intent into complete statistical code.

    "Test if charity density affects SNAP enrollment using multi-level model"
    → Full Stata/R script with diagnostics, citations, and self-healing logic.
    """
    model_chain = model_chain or [os.environ.get("EDITH_MODEL", "gemini-2.5-flash")]
    intent_lower = intent.lower()

    # Match to template
    matched_template = None
    for name, template in INTENT_TEMPLATES.items():
        if any(kw in intent_lower for kw in template["keywords"]):
            matched_template = (name, template)
            break

    # Sign the thought
    sig = thought_signer.sign_thought(
        f"Tab-to-Intent: {intent}",
        grounding=[matched_template[0] if matched_template else "custom"],
    )

    # Generate code via LLM
    try:
        from server.backend_logic import generate_text_via_chain

        template_hint = ""
        if matched_template:
            key = f"{language}_template"
            template_hint = f"\n\nUSE THIS TEMPLATE AS A STARTING POINT:\n{matched_template[1].get(key, '')}"

        prompt = (
            f"RESEARCH INTENT: {intent}\n"
            f"LANGUAGE: {language}\n"
            f"DATA PATH: {data_path or 'placeholder.dta'}\n"
            f"{template_hint}\n\n"
            f"Generate a COMPLETE, RUNNABLE script that:\n"
            f"1. Loads and cleans the data\n"
            f"2. Runs descriptive statistics\n"
            f"3. Tests the hypothesis with appropriate methods\n"
            f"4. Includes all necessary diagnostics\n"
            f"5. Adds comments explaining each step\n"
            f"6. Handles common errors (singular matrix, convergence)\n\n"
            f"Output the FULL script, ready to run."
        )
        code, model = generate_text_via_chain(
            prompt, model_chain,
            system_instruction=(
                "You are a statistical programming expert for political science. "
                "Generate production-quality code with robust error handling. "
                "Use clustered standard errors by default. "
                "Always check assumptions before running models."
            ),
            temperature=0.1,
        )

        return {
            "intent": intent,
            "language": language,
            "template_used": matched_template[0] if matched_template else "custom",
            "code": code,
            "thought_signature": sig["signature"],
            "model": model,
            "requires_approval": True,
        }
    except Exception as e:
        # Fallback: use template directly
        if matched_template:
            key = f"{language}_template"
            code = matched_template[1].get(key, f"* Intent: {intent}")
            return {
                "intent": intent,
                "language": language,
                "template_used": matched_template[0],
                "code": code,
                "thought_signature": sig["signature"],
                "model": "template_fallback",
                "requires_approval": True,
            }
        return {"error": str(e)}


